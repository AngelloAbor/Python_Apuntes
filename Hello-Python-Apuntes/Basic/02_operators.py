#Operadores.

#aritmeticos
print (8 + 5)
print (8 - 5)
print (8 * 5)
print (8 / 5)
print (10 % 3)
print (10 // 3)
print (2 ** 3)

print("hola " + "python")
print("hola " * 4)

#operadores comparativos.

print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

#podemos comparar texto bajo la premisa de una ordenacion alfabetica 
print("hola" > "mundo")
print("hola" < "mundo")
print("hola" >= "mundo")
print("hola" <= "mundo")
print("hola" == "hola")
print("hola" != "hola")

#operadores logicos
print(3 > 4 and "hola" > "mundo")
print(5 > 4 or "hola" > "mundo")
print(not(3>4))